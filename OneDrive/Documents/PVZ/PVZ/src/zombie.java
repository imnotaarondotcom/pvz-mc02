public class Zombie {
    protected int speed;
    protected int health;
    protected int damage;

    public int getSpeed(){
        return speed;
    }

    public int getHealth(){
        return health;
    }

    public int getDamage(){
        return damage;
    }

    public void setSpeed(int s){
        speed = s; 
    }

    public void setHealth(int h){
        health = h; 
    }

    public void setDamage(int d){
        damage = d;
    }
    
}
